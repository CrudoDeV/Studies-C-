using System;

namespace Numero_par
{
    class MainClass
    {
        public static void Main(string[] args)
        {
            int n;

            Console.WriteLine("Digite um número");
            n= int.Parse(Console.ReadLine());

            if (n % 2==0)
            {
                Console.WriteLine("O número é Par");

            }

            if (n % 2 == 1) {
                Console.WriteLine("O número é ímpar");
            }

            Console.ReadLine();

        }
    }
}
